package it.beccaria.petstore.provini;

public class Magistrato {

	private Long id;
	private String cognome;
	private String nome;
	private String dataInizioAssociazioneUfficio;
	private String dataFineAssociazioneUfficio;
	private String idUfficio;

	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getCognome() {
		return cognome;
	}
	public void setCognome(String cognome) {
		this.cognome = cognome;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getDataInizioAssociazioneUfficio() {
		return dataInizioAssociazioneUfficio;
	}
	public void setDataInizioAssociazioneUfficio(String dataInizioAssociazioneUfficio) {
		this.dataInizioAssociazioneUfficio = dataInizioAssociazioneUfficio;
	}
	public String getDataFineAssociazioneUfficio() {
		return dataFineAssociazioneUfficio;
	}
	public void setDataFineAssociazioneUfficio(String dataFineAssociazioneUfficio) {
		this.dataFineAssociazioneUfficio = dataFineAssociazioneUfficio;
	}
	public String getIdUfficio() {
		return idUfficio;
	}
	public void setIdUfficio(String idUfficio) {
		this.idUfficio = idUfficio;
	}




}
